fun main() {
    // Запрашиваем у пользователя число N
    print("Введите число N: ")
    val n = readLine()!!.toInt()

    // Инициализируем переменные для хранения сумм
    var sumEven = 0 // Сумма четных чисел
    var sumOdd = 0 // Сумма нечетных чисел

    // Цикл для перебора чисел от 1 до N
    for (i in 1..n) {
        // Проверяем, четное ли число
        if (i % 2 == 0) {
            // Если четное, добавляем его к сумме четных чисел
            sumEven += i
        } else {
            // Если нечетное, добавляем его к сумме нечетных чисел
            sumOdd += i
        }
    }

    // Выводим результаты
    println("Сумма четных чисел: $sumEven")
    println("Сумма нечетных чисел: $sumOdd")
}