fun main() {
    // Запрашиваем у пользователя число.
    println("Введите число:")
    val number = readLine()!!.toInt()

    // Проверяем, неотрицательное ли число.
    if (number < 0) {
        println("Введите неотрицательное число.")
        return
    }

    // Инициализируем переменную для хранения суммы цифр.
    var sum = 0

    // Используем цикл для перебора всех цифр в числе.
    var temp = number
    while (temp > 0) {
        // Извлекаем последнюю цифру числа.
        val digit = temp % 10

        // Добавляем цифру к сумме.
        sum += digit

        // Удаляем последнюю цифру из числа.
        temp /= 10
    }

    // Выводим результат.
    println("Сумма цифр числа $number равна $sum")
}