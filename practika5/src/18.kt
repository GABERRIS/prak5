fun main() {
    // Запрашиваем у пользователя высоту лестницы
    print("Введите высоту лестницы: ")
    val height = readLine()!!.toInt()

    // Проверяем, чтобы высота была положительной
    if (height <= 0) {
        println("Высота лестницы должна быть больше 0")
        return
    }

    // Рисуем лестницу
    for (i in 1..height) {
        // Выводим пробелы перед символами "#"
        for (j in 1..height - i) {
            print(" ")
        }
        // Выводим символы "#"
        for (k in 1..i) {
            print("#")
        }
        // Переходим на новую строку
        println()
    }
}